#ifndef BULLET_H
#define BULLET_H

#include <glm.hpp>

class Bullet
{
private:

    //--------------------------------------------------
    // Transform
    //--------------------------------------------------

    glm::vec2 position;

    glm::vec2 direction;

    //--------------------------------------------------
    // Movement
    //--------------------------------------------------

    float speed;

    //--------------------------------------------------
    // Size
    //--------------------------------------------------

    float width;

    float height;

    //--------------------------------------------------
    // Ownership
    //--------------------------------------------------

    bool fromPlayer;

    //--------------------------------------------------
    // State
    //--------------------------------------------------

    bool active;

public:

    //--------------------------------------------------
    // Constructor
    //--------------------------------------------------

    Bullet();

    //--------------------------------------------------
    // Position
    //--------------------------------------------------

    void SetPosition(
        const glm::vec2& newPosition);

    glm::vec2 GetPosition() const;

    //--------------------------------------------------
    // Direction
    //--------------------------------------------------

    void SetDirection(
        const glm::vec2& newDirection);

    glm::vec2 GetDirection() const;

    //--------------------------------------------------
    // Speed
    //--------------------------------------------------

    void SetSpeed(
        float newSpeed);

    float GetSpeed() const;

    //--------------------------------------------------
    // Size
    //--------------------------------------------------

    void SetSize(
        float newWidth,
        float newHeight);

    float GetWidth() const;

    float GetHeight() const;

    //--------------------------------------------------
    // Ownership
    //--------------------------------------------------

    void SetFromPlayer(
        bool value);

    bool IsFromPlayer() const;

    //--------------------------------------------------
    // State
    //--------------------------------------------------

    bool IsActive() const;

    void Deactivate();

    //--------------------------------------------------
    // Bounds
    //--------------------------------------------------

    glm::vec2 GetMinBounds() const;

    glm::vec2 GetMaxBounds() const;

    //--------------------------------------------------
    // Update
    //--------------------------------------------------

    void Update(
        float deltaTime);

    //--------------------------------------------------
    // Screen Test
    //--------------------------------------------------

    bool IsOutsideScreen(
        int screenWidth,
        int screenHeight) const;

    //--------------------------------------------------
    // Destructor
    //--------------------------------------------------

    ~Bullet();
};

#endif